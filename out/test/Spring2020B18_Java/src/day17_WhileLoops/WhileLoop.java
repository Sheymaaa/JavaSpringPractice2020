package day17_WhileLoops;

public class WhileLoop {
    public static void main(String[] args) {
        for(int i = 0; i <= 5; i++){
            System.out.println(i);
        }

        System.out.println("==========================");
        int i = 6;
        while(i>=5){
            System.out.println(i);
        }

    }

}